from dataclasses import dataclass, field
from typing import Optional
from autosar.models.limit import Limit

__NAMESPACE__ = "http://autosar.org/schema/r4.0"


@dataclass
class ClientIdRange:
    """
    With this element it is possible to restrict the Client Identifier of the
    transaction handle that is generated by the client RTE for inter-Ecu
    Client/Server communication to an allowed range of numerical values.

    :ivar lower_limit: This specifies the lower limit of the
        ClientIdRange.
    :ivar upper_limit: This specifies the upper limit of the
        ClientIdRange.
    :ivar s: Checksum calculated by the user's tool environment for an
        ArObject. May be used in an own tool environment to determine if
        an ArObject has changed. The checksum has no semantic meaning
        for an AUTOSAR model and there is no requirement for AUTOSAR
        tools to manage the checksum.
    :ivar t: Timestamp calculated by the user's tool environment for an
        ArObject. May be used in an own tool environment to determine
        the last change of an ArObject. The timestamp has no semantic
        meaning for an AUTOSAR model and there is no requirement for
        AUTOSAR tools to manage the timestamp.
    """
    class Meta:
        name = "CLIENT-ID-RANGE"

    lower_limit: Optional[Limit] = field(
        default=None,
        metadata={
            "name": "LOWER-LIMIT",
            "type": "Element",
            "namespace": "http://autosar.org/schema/r4.0",
        }
    )
    upper_limit: Optional[Limit] = field(
        default=None,
        metadata={
            "name": "UPPER-LIMIT",
            "type": "Element",
            "namespace": "http://autosar.org/schema/r4.0",
        }
    )
    s: Optional[str] = field(
        default=None,
        metadata={
            "name": "S",
            "type": "Attribute",
        }
    )
    t: Optional[str] = field(
        default=None,
        metadata={
            "name": "T",
            "type": "Attribute",
            "pattern": r"([0-9]{4}-[0-9]{2}-[0-9]{2})(T[0-9]{2}:[0-9]{2}:[0-9]{2}(Z|([+\-][0-9]{2}:[0-9]{2})))?",
        }
    )
