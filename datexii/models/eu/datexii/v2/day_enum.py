from enum import Enum

__NAMESPACE__ = "http://datex2.eu/schema/2/2_0"


class DayEnum(Enum):
    """
    Days of the week.

    :cvar MONDAY: Monday.
    :cvar TUESDAY: Tuesday.
    :cvar WEDNESDAY: Wednesday.
    :cvar THURSDAY: Thursday.
    :cvar FRIDAY: Friday.
    :cvar SATURDAY: Saturday.
    :cvar SUNDAY: Sunday.
    """
    MONDAY = "monday"
    TUESDAY = "tuesday"
    WEDNESDAY = "wednesday"
    THURSDAY = "thursday"
    FRIDAY = "friday"
    SATURDAY = "saturday"
    SUNDAY = "sunday"
